int load_font(const char* file, afx_font_t* font)
{
    FILE *fp = fopen(file,"rb");
    if(!fp)
    {
        font->loaded = 0;
        perror("fopen");
        return 0;
    } 
    uint8_t header[8];
    uint16_t v;
    fread(header,8,1,fp);
    v = *((uint16_t*)header);
    if(v != 0xAF8D)
    {
        font->loaded = 0;
        LOG("Wrong magic header \n");
        fclose(fp);
         return 0;
    }
    font->first = header[2];
    font->last = header[3];
    font->yAdvance = header[4];
    memcpy(&font->size,header+5,2);
    // read the glynph table
    font->gsize = 95; 
    v = font->gsize*sizeof(afx_font_glyph_t);
    font->glyphs = (afx_font_glyph_t*) malloc(v);
    v = fread(font->glyphs,1,v,fp);
    font->data = (uint8_t*) malloc(font->size);
    v = fread(font->data,1,font->size,fp);

    if(v != font->size)
    {
        font->loaded = 0;
        LOG("Cannot read all bitmap data %d/%d\n",v,font->size);
        fclose(fp);
        if(font->glyphs) 
        {
            free(font->glyphs);
            font->glyphs = NULL;
        }

        if(font->data){
            free(font->data);
            font->data = NULL;
        }
        return 0;
    }
    font->loaded = 1;
    return 1;
}
void font_release(afx_font_t* font)
{
    font->loaded = 0;
    if(font->glyphs)
    {
        free(font->glyphs);
        font->glyphs = NULL;
    }
    if(font->data)
    {
        free(font->data);
        font->data = NULL;
    }
}